
function instafeature1(){
 let icon = document.querySelector("#icon")
  icon.style.scale = "1.30"
  icon.style.opacity = "1"
  icon.style.transition = "0.40s"
  
  setTimeout(function(){
  icon.style.scale = "1"
   icon.style.opacity = "0"
  },
  1500)
 }

